#pragma config EBTRB = OFF      // Boot Block Table Read Protection bit (Boot block (000000-0007FFh) is not protected from table reads executed in other blocks)

#define CONFIG_TIMING_MAIN_CLOCK 12000000

// Tick values
uint32_t lastrun = 0;
// PID Variables
float in, out, set = 100;
// PID controllers
struct pid_controller pidctrl;
pid_t pid = 0;
// SPI Resource
xSPIHandle spi;
// Thermocouples
tc_t sensor1;
tc_t sensor2;

int main(int argc, char** argv)
{
	spi = spi_init(E_SPI_1);
	spi_control(spi, SPI_MASTER | SPI_MODE_0, SPI_DIV_1_64);

	//Create thermocuples CS pin on RB2 and RB3
	sensor1 = tc_init(spi, 10);
	sensor2 = tc_init(spi, 11);

	// Create PID controllers, set gains
	pid = pid_create(&pidctrl, &in, &out, &set, 5, 1, 3);
	// Set output limits
	pid_limits(pid, 0, 255);
	// Turn on PID
	pid_auto(pid);

	for (;;) {
		if (tick_get() - lastrun >= TICK_SECOND) {
			lastrun = tick_get();
			// Read inputs
			in = tc_read_float(&sensor1);
			// Compute PID controllers
			pid_compute(pid);
			// Adjust actuator
			// set_out(output);
		}
	}

}

